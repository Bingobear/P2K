package master.keyEx.models;

public class Keyword {
	private int score;
	private String word;
	
	public Keyword() {

	}
	
	public int getScore() {
		return score;
	}

	public void setScore(int score) {
		this.score = score;
	}

	public String getWord() {
		return word;
	}

	public void setWord(String word) {
		this.word = word;
	}	
}
